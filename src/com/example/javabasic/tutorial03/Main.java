package com.example.javabasic.tutorial03;

public class Main {

    public static void main(String[] args) {
        for(char i = 'a'; i <= 'z'; i++) {
            System.out.print(i + " "); //a부터 z까지 출력
        }
    }
}
//<자료형>
//내부적으로 a부터 z까지는 ASCII 코드를 사용해서 컴퓨터 내부적으로 1씩 증가하면서 알파벳이 증가한다.
//ASCII 코드 => A : 65, a : 97, b는 98

